''' This module contains all the functionality of the calculator'''


def add(x, y):
    ''' This function adds two numbers'''
    return x + y


def sub(x, y):
    ''' This function subtracts two numbers'''
    return x - y


def mul(x, y):
    ''' This function multiplies two numbers'''
    return x * y


def div(x, y):
    ''' This function divides two numbers'''
    return x / y
